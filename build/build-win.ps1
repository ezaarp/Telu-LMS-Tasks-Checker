$ErrorActionPreference = "Stop"

$projectRoot = Split-Path -Parent $PSScriptRoot
$distDir = Join-Path $projectRoot "dist"
$winUnpackedDir = Join-Path $distDir "win-unpacked"
$targetExeName = "TelU LMS Tasks Checker.exe"
$electronExePath = Join-Path $winUnpackedDir "electron.exe"
$targetExePath = Join-Path $winUnpackedDir $targetExeName
$resourcesDir = Join-Path $winUnpackedDir "resources"
$appAsarPath = Join-Path $resourcesDir "app.asar"
$appUpdateConfigPath = Join-Path $resourcesDir "app-update.yml"
$bundleTempDir = Join-Path $distDir "app-bundle-temp"
$nodeExe = "C:\Program Files\nodejs\node.exe"
$builderCli = Join-Path $projectRoot "node_modules\electron-builder\cli.js"
$asarCli = Join-Path $projectRoot "node_modules\@electron\asar\bin\asar.js"

$env:PATH = "C:\Program Files\nodejs;$env:SystemRoot\System32;$env:PATH"

Write-Host "Building unpacked Windows app..."
$dirBuildSucceeded = $true
try {
  & $nodeExe $builderCli --dir
} catch {
  $dirBuildSucceeded = $false
  Write-Warning "electron-builder --dir returned a non-zero exit code. Continuing because win-unpacked may already be available."
}

if (-not (Test-Path $winUnpackedDir)) {
  throw "win-unpacked was not generated."
}

if (Test-Path $electronExePath) {
  if (Test-Path $targetExePath) {
    Remove-Item $targetExePath -Force
  }
  Rename-Item $electronExePath $targetExeName -Force
}

if (-not (Test-Path $targetExePath)) {
  throw "Expected executable '$targetExeName' was not found in win-unpacked."
}

if (-not (Test-Path $resourcesDir)) {
  New-Item -ItemType Directory -Path $resourcesDir | Out-Null
}

if (-not (Test-Path $appAsarPath)) {
  Write-Warning "app.asar was not generated by electron-builder. Creating a fallback bundle..."

  if (Test-Path $bundleTempDir) {
    Remove-Item $bundleTempDir -Recurse -Force
  }

  New-Item -ItemType Directory -Path $bundleTempDir | Out-Null
  Copy-Item (Join-Path $projectRoot "src") (Join-Path $bundleTempDir "src") -Recurse -Force
  Copy-Item (Join-Path $projectRoot "package.json") (Join-Path $bundleTempDir "package.json") -Force

  if (Test-Path (Join-Path $projectRoot "README.md")) {
    Copy-Item (Join-Path $projectRoot "README.md") (Join-Path $bundleTempDir "README.md") -Force
  }

  & $nodeExe "C:\Program Files\nodejs\node_modules\npm\bin\npm-cli.js" install --omit=dev --ignore-scripts --no-package-lock --silent --prefix $bundleTempDir
  & $nodeExe $asarCli pack $bundleTempDir $appAsarPath
}

@"
provider: github
owner: ezaarp
repo: TelU-LMS-Tasks-Checker
updaterCacheDirName: telu-lms-widget-updater
"@ | Set-Content -Path $appUpdateConfigPath -Encoding UTF8

Write-Host "Packaging installer and portable build..."
& $nodeExe $builderCli --prepackaged $winUnpackedDir --win nsis portable

if (-not $dirBuildSucceeded) {
  Write-Warning "The initial unpacked build still reported an electron-builder error, but packaging completed from the repaired win-unpacked output."
}
